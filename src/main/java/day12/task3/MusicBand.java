package day12.task3;

class MusicBand {
    private String name; //название группы
    private int year;// год основания

    public MusicBand(String name, int year) {
        this.name = name;
        this.year = year;
    }

    public String getName() {
        return name;
    }

    public int getYear() {
        return year;
    }
}
