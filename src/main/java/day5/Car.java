package day5;

class Car{
    private int year;
    private String color;
    private String model;

    public int getYear() {
        return year;
    }

    public String getColor() {
        return color;
    }

    public String getModel() {
        return model;
    }

    public void setYear(int ageAuto) {
        year = ageAuto;
    }

    public void setColor(String colorAuto) {
        color = colorAuto;

    }

    public void setModel(String modelAuto) {
        model = modelAuto;
    }
}
